package String;

//Substring in java

public class StringExample12 {

    public static void main(String[] args) {

        String s = "String 1";
        System.out.println("Original string is " + s);
        System.out.println("Substring starting from index 3 is " + s.substring(3));
        System.out.println("Substring starting from index 0 to 4 is " + s.substring(0,4));
    }
}
